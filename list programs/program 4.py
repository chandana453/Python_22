'''
python program to split even and odd elements into two lists python
solution:
'''
x=[3,75,8,9,4,388]
evenl=[]
oddl=[]
for i in range(len(x)):
    if x[i]%2==0:
        evenl.append(x[i])
    else:
        oddl.append(x[i])
print(evenl)
print(oddl)

